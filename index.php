      <div class="window--container">
        <p>Supervision ist eine Beratungsform für die berufliche Arbeit. Sie dient der Verbesserung von Arbeitsbeziehungen und -abläufen.</p>
        <p>Supervision eröffnet neue Perspektiven und erweitert fachliche und soziale Kompetenzen zu eigenverantwortlichem Handeln.</p>
        <p>Supervision verstehe ich als eine gemeinsame Such-Bewegung, die das Lernen von Einzelpersonen, Gruppen, Teams und Organisationen unterstützt.</p>
        <p>Supervision befördert die Reflexion des beruflichen Alltags, die Entwicklung der Berufsrolle und der beruflichen Perspektiven, die Wahrnehmungs-, Kommunikations- und Kooperationsfähigkeit von Einzelnen,  Gruppen, Teams und Organisationen, die Verbesserung im Umgang mit Konflikten und deren Lösung und Veränderungsprozesse.</p>
        <p>Im Mittelpunkt meiner Arbeit steht die Begleitung der Menschen, die auf ihrem Weg  mehr Zufriedenheit, innere Klarheit und persönliches Wachstum erfahren wollen.</p>
        <blockquote>
          <p>Ein Wunder ist nicht auf dem Wasser zu gehen. Ein Wunder ist es, im gegenwärtigen Moment auf der grünen Erde zu gehen, den Frieden und die Schönheit zu schätzen, die genau jetzt verfügbar sind… dies zu erleben ist keine Frage des Glaubens, es ist eine Frage der Übung.</p>
          <em>Tich Nhat Hanh</em>
        </blockquote>
